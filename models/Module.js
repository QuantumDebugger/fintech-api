const mongoose = require('mongoose');

const ModuleSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      unique: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);

const Module = mongoose.model('Module', ModuleSchema);
module.exports = Module;